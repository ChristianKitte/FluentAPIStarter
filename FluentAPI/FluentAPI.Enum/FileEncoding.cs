namespace FluentAPI.Enum
{
    /// <summary>
    /// Legt die Codierung fest, mit der ausgegeben wird
    /// </summary>
    public enum FileEncoding
    {
        /// <summary>
        ///  Codierung der Datei verwenden
        /// </summary>
        Auto,

        /// <summary>
        /// Ausgabe in utf 8 codieren
        /// </summary>
        Utf8,

        /// <summary>
        /// Ausgabe in utf 16 codieren
        /// </summary>
        Utf16,

        /// <summary>
        /// Ausgabe in utf 32 codieren
        /// </summary>
        Utf32
    }
}